import Foundation
import SwiftUI

// タイピングミスを減らすために Color に独自のプロパティを設定
extension Color {
    static let backGroundColorGray = Color("BackGroundColorGray")
    static let cardColorGray = Color("CardColorGray")
    static let uiColorGray = Color("UIColorGray")
    static let uiColorGreen = Color("UIColorGreen")
    static let uiColorRed = Color("UIColorRed")
    static let uiColorWhite = Color("UIColorWhite")
    static let uiColorYellow = Color("UIColorYellow")
}
